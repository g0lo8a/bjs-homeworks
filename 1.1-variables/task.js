/**
 * Задача № 3

 Задача со "звездочкой"
 Ученику 7 класса ”Б” Васе очень не нравится решать примеры, зато ему нравится программирование. Но пока он не очень
 хорошо разбирается в нем. Помогите Васе в трудной ситуации и напишите программу,
 которая решит следующий пример: x * y + 5 * z + x - 1, где x = 2, y = 22, z = 0 и выведет результат на экран.
 */

function calculateFormula() {
    let x = 2,
        y = 22,
        z = 0;
    return x * y + 5 * z + x - 1;
}
