/**
 * Задача № 1
 *
 Мария Степановна работает завучем в школе. Все бы ничего, но ее совсем замучили бумажные журналы, и она решила сделать
 электронный журнал. Решила Мария Степановна начать с расчета средней оценки ученика по предметам. Помогите Марии написать
 программу, которая сможет подсчитать среднюю оценку за 3 занятия и вывести результат.
 */

function averageMark(a, g, p) {
    return (a + g + p) / 3;
}

